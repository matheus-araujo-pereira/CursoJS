// Strings
// String: É uma sequência de caracteres
// String: É um tipo de dado primitivo
// String: É um tipo de dado que representa texto
// String: É um tipo de dado que pode ser criado com aspas simples, aspas duplas ou crase
console.log('Matheus Araujo "Pereira"');
console.log("Matheus 'Araujo' Pereira");
console.log(`'Matheus' "Araujo" Pereira`);

// Number
// Number: É um tipo de dado primitivo
// Number: É um tipo de dado que representa números
// Number: É um tipo de dado que pode ser inteiro ou decimal
console.log(123456);
console.log(123456.789);
console.log(123456.789e5);
// Number: É um tipo de dado que pode ser positivo ou negativo
console.log(-123456);
console.log(-123456.789);
// Number: É um tipo de dado que pode ser infinito
console.log(Infinity);
console.log(-Infinity);
// Number: É um tipo de dado que pode ser NaN (Not a Number)
console.log(NaN);
// Number: É um tipo de dado que pode ser um número muito grande
// Number: É um tipo de dado que pode ser um número muito pequeno
console.log(123456, 'Matheus', 12345678901234567890.0005, "Developer");